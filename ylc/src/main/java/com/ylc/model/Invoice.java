package com.ylc.model;

public class Invoice {
	private String paymentID;
	private String paymentDate;
	private String paidToNumber;
	private String noOfMin;
	private String add;
	private String agent;
	private String amount;
	private String edit;
	private String delete;
	public String getPaymentID() {
		return paymentID;
	}
	public void setPaymentID(String paymentID) {
		this.paymentID = paymentID;
	}
	public String getPaymentDate() {
		return paymentDate;
	}
	public void setPaymentDate(String paymentDate) {
		this.paymentDate = paymentDate;
	}
	public String getPaidToNumber() {
		return paidToNumber;
	}
	public void setPaidToNumber(String paidToNumber) {
		this.paidToNumber = paidToNumber;
	}
	public String getNoOfMin() {
		return noOfMin;
	}
	public void setNoOfMin(String noOfMin) {
		this.noOfMin = noOfMin;
	}
	public String getAdd() {
		return add;
	}
	public void setAdd(String add) {
		this.add = add;
	}
	public String getAgent() {
		return agent;
	}
	public void setAgent(String agent) {
		this.agent = agent;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getEdit() {
		return edit;
	}
	public void setEdit(String edit) {
		this.edit = edit;
	}
	public String getDelete() {
		return delete;
	}
	public void setDelete(String delete) {
		this.delete = delete;
	}
	

}
